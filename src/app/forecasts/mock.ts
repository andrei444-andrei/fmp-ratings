// ── Поквартальная раскадровка ──────────────────────────────────────────────
// Прогноз ИБ — годовой; квартальную «раскадровку» строим из годового ФАКТА
// детерминированным разложением: 4 квартальные доходности компаундируются ровно
// в годовую ((1+q1)(1+q2)(1+q3)(1+q4) = 1+real). Форма пути варьируется по
// (стране, году) — чтобы было видно реальную траекторию, а не равномерную.
// Детерминировано (без Math.random) — прототип стабилен между перезагрузками.

function hash(s: string): number {
  let h = 2166136261;
  for (let i = 0; i < s.length; i++) { h ^= s.charCodeAt(i); h = Math.imul(h, 16777619); }
  return (h >>> 0) / 4294967295; // 0..1
}

// Веса лог-доходности по кварталам (сумма = 1), форма зависит от seed.
function quarterWeights(seed: number): [number, number, number, number] {
  const shapes: [number, number, number, number][] = [
    [0.40, 0.10, 0.20, 0.30],
    [0.15, 0.35, 0.30, 0.20],
    [0.30, 0.30, 0.10, 0.30],
    [0.10, 0.25, 0.40, 0.25],
    [0.35, 0.25, 0.25, 0.15],
    [0.20, 0.15, 0.30, 0.35],
  ];
  return shapes[Math.floor(seed * shapes.length) % shapes.length];
}

// 4 квартальные доходности, компаундирующиеся в годовую `real`.
export function quarterize(countryCode: string, year: number, real: number): number[] {
  const w = quarterWeights(hash(countryCode + ':' + year));
  const logTotal = Math.log(1 + real);
  return w.map((wi) => Math.exp(wi * logTotal) - 1);
}

// Накопительный путь по кварталам (YTD): [после Q1, после Q2, Q3, Q4].
export function cumulativePath(quarters: number[]): number[] {
  const out: number[] = [];
  let acc = 1;
  for (const q of quarters) { acc *= 1 + q; out.push(acc - 1); }
  return out;
}
