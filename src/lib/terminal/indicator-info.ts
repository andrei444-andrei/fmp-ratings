/** Находит описание индикатора по названию события (самое длинное совпадение подстроки match). */
export function lookupIndicator(eventName: string): IndicatorInfo | null {
  const s = String(eventName || '').toLowerCase();
  if (!s) return null;
  let best: IndicatorInfo | null = null;
  let bestLen = 0;
  for (const ind of INDICATORS) {
    for (const m of ind.match) {
      if (m && m.length > bestLen && s.includes(m)) {
        best = ind;
        bestLen = m.length;
      }
    }
  }
  return best;
}
